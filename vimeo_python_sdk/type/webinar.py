from datetime import datetime, date
import typing
from enum import Enum
from typing_extensions import TypedDict, Literal, TYPE_CHECKING

from vimeo_python_sdk.type.email_capture_form import EmailCaptureForm
from vimeo_python_sdk.type.user import User
from vimeo_python_sdk.type.webinar_edit import WebinarEdit
from vimeo_python_sdk.type.webinar_email_provider_list import WebinarEmailProviderList
from vimeo_python_sdk.type.webinar_email_quota import WebinarEmailQuota
from vimeo_python_sdk.type.webinar_email_settings import WebinarEmailSettings
from vimeo_python_sdk.type.webinar_events import WebinarEvents
from vimeo_python_sdk.type.webinar_metadata import WebinarMetadata
from vimeo_python_sdk.type.webinar_privacy import WebinarPrivacy
from vimeo_python_sdk.type.webinar_registration_data import WebinarRegistrationData
from vimeo_python_sdk.type.webinar_schedule import WebinarSchedule

class RequiredWebinar(TypedDict):
    # The title of the webinar.
    title: typing.Optional[str]

    # The description of the webinar.
    description: typing.Optional[str]

    # The time in ISO 8601 format when the webinar was completed.
    completed_on: str

    # The time in ISO 8601 format when the webinar was created.
    created_time: str

    edit: WebinarEdit

    email_provider_list: WebinarEmailProviderList

    email_quota: WebinarEmailQuota

    # The settings for emails that are sent about the webinar.
    email_settings: WebinarEmailSettings

    events: WebinarEvents

    # Whether polls are associated with the webinar.
    has_polls: bool

    metadata: WebinarMetadata

    # The time in ISO 8601 format when the webinar was modified.
    modified_on: str

    # The date in ISO 8601 format on which the next occurrence of the webinar is expected to be live.
    next_occurrence_time: typing.Optional[str]

    # The password used to access the videos generated by streaming to the webinar event.
    password: typing.Optional[str]

    privacy: WebinarPrivacy

    registration_data: WebinarRegistrationData

    # The registration form settings associated with the webinar.
    registration_form: EmailCaptureForm

    schedule: WebinarSchedule

    # The status of the webinar.  Option descriptions:  * `ended` - The webinar has ended.  * `started` - The webinar has started. 
    status: typing.Optional[str]

    # The time zone used in resolving the timestamps that are included in the automatically generated video titles for the webinar.
    time_zone: str

    # The webinar's canonical relative URI.
    uri: str

    # The owner of the webinar.
    user: User

class OptionalWebinar(TypedDict, total=False):
    pass

class Webinar(RequiredWebinar, OptionalWebinar):
    pass
